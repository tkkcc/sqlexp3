-- drop table if exists customer,manager,store,warehouse,department,employee,room,checkin,consumption,bill;
drop table if exists bill,consumption,checkin,room,employee,department,warehouse,store,manager,customer;
create table manager (
    id int not null auto_increment,
    name varchar(191),
    primary key(id)
);
create table store (
    id int not null auto_increment,
    managerId int,
    location varchar(191),
    primary key(id),
    foreign key(managerId) references manager(id)
);
create table warehouse (
    id int not null auto_increment,
    type varchar(191),
    storeId int,
    manager varchar(191),
    location varchar(191),
    primary key(id),
    foreign key(storeId) references store(id)
);

create table department (
    id int not null auto_increment,
    storeId int,
    type varchar(191),
    manager varchar(191),
    primary key(id,storeId),
    foreign key(storeId) references store(id)
);
create table employee (
    id int not null auto_increment,
    departmentId int,
    storeId int,
    name varchar(191),
    contact varchar(191),
    address varchar(191),
    salary varchar(191),
    type varchar(191),
    duration varchar(191),
    primary key(id,departmentId,storeId),
    foreign key(storeId) references store(id),
    foreign key(departmentId) references department(id)
);
create table room (
    id int not null auto_increment,
    storeId int,
    level varchar(191),
    num varchar(191),
    price varchar(191),
    status varchar(191),
    primary key(id,storeId),
    foreign key(storeId) references store(id)
);
create table customer (
    id int not null auto_increment,
    name varchar(191),
    contact varchar(191),
    identification varchar(191),
    primary key(id)
);

